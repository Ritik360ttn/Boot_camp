class UserInfo {

    static String firstname;
    static String LastName;

    static int Age;


    static {

        firstname = "Ritik";
        LastName = "Kumar";
        Age = 22;
        System.out.println("showing from static block");
        System.out.println("Firstname:" + firstname + "     Lastname:" + LastName + "     Age:" + Age);
    }


    public static void info(String fn, String ln, int age) {
        firstname = fn;
        LastName = ln;
        Age = age;
        System.out.println("showing from static method");
        System.out.println("Firstname:" + firstname + "     Lastname:" + LastName + "     Age:" + Age);

    }


    public static void main(String[] args) {

        UserInfo u = new UserInfo();
        info("naman", "mathur", 29);
    }
}
